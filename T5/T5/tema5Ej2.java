//Muestra los números múltiplos de 5 de 0 a 100 utilizando un bucle while.

public class tema5Ej2 {
  public static void main (String[]args) {
    
    int i=0;
    
    System.out.println("Multiplos de 5 hasta llegar a 100");
  
    while(i<=100){
      System.out.println(i);
      i +=5;
    } 
  
  }
  
}
